def emit_remaining_functions(
    lines: list[str], functions: list[dict], api_calls: list[dict], xrefs: dict
) -> None:
    special = {
        0x81C00020, 0x81C00050, 0x81C001A0, 0x81C002F8, 0x81C004BC,
        0x81C00648, 0x81C00654, 0x81C0069C, 0x81C00F7C, 0x81C01204,
        0x81C027D0, 0x81C02854,
        0x81C0280C, 0x81C02884, 0x81C02AD8, 0x81C02AF8, 0x81C02B60,
        0x81C02BD0, 0x81C03480, 0x81C034A0,
        0x81C03320, 0x81C034D0, 0x81C034F0, 0x81C03668, 0x81C036B8,
        0x81C036E0,
        0x81C006D0, 0x81C014DC, 0x81C0218C, 0x81C0240C, 0x81C02598,
        0x81C026C0,
    }
    apis_by_function: dict[int, list[dict]] = defaultdict(list)
    for call in api_calls:
        apis_by_function[int(call["function_va"])].append(call)

    calls = xrefs.get("direct_calls", [])
    strings = xrefs.get("string_xrefs", [])
    for function in functions:
        va = int(function["va"])
        if va in special:
            continue
        start = int(function["file_offset"])
        end = int(function["candidate_end_off"])
        name = name_for_va(va)
        if name.endswith("_block"):
            continue
        direct = [c for c in calls if start <= int(c["file_offset"]) < end]
        refs = [s for s in strings if start <= int(s["use_off"]) < end]
        confidence = function.get("confidence", "unknown")
        description = FUNCTION_COMMENTS.get(va)
        if description is None and confidence == "call_target_or_api_context":
            description = (
                "窗口过程内部的候选代码块；名称按该块调用的系统 API 和邻近控制流推测，"
                "不应视为独立 C 函数。"
            )
        elif description is None:
            description = "函数用途按调用关系和指令形状推测，仍需动态验证。"
        lines.extend(
            [
                f"/* {description}",
                f" * 边界可信度：{confidence}；原始地址：0x{va:08x}。",
                " */",
                f"static word_t {name}(void)",
                "{",
                f"    /* file+0x{start:x}..0x{end:x}, VA 0x{va:08x}, {end - start} bytes.",
            ]
        )
        if apis_by_function[va]:
            lines.append("     * Recovered API call order:")
            for call in sorted(apis_by_function[va], key=lambda item: int(item["jalr_off"])):
                names = "/".join(call.get("sdk_names", [])) or "unknown"
                lines.append(
                    f"     *   file+0x{int(call['jalr_off']):x}: {call['table']}+0x{int(call['api_offset']):03x} {names}"
                )
        if direct:
            lines.append("     * Direct internal calls:")
            for call in direct:
                target = int(call["target_va"])
                lines.append(
                    f"     *   file+0x{int(call['file_offset']):x}: {name_for_va(target)} (0x{target:08x})"
                )
        if refs:
            lines.append("     * Referenced strings:")
            for ref in refs:
                lines.append(
                    f'     *   file+0x{int(ref["use_off"]):x}: "{short_text(ref["text"])}"'
                )
        lines.extend(
            [
                "     * Structured branches, argument types and return type remain uncertain.",
                "     */",
                f"    return unresolved(0x{va:08x}u);",
                "}",
                "",
            ]
        )


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate a C-like reconstruction of Element Table BDA")
    parser.add_argument("analysis_dir", type=Path)
    parser.add_argument("--output", type=Path)
    args = parser.parse_args()

    root = args.analysis_dir
    functions_doc = load_json(root / "element_table_functions.json")
    api_doc = load_json(root / "element_table_api_calls.json")
    strings_doc = load_json(root / "element_table_strings_interesting.json")
    xrefs_doc = load_json(root / "element_table_xrefs.json")
    output = args.output or root / "element_table_reconstructed.c"
    system_header = output.with_name("system.h")
    source = Path(functions_doc["source"])
    if not source.is_absolute():
        source = root.parents[1] / source
    navigation_pairs = load_navigation_pairs(source)

    functions = functions_doc["functions"]
    lines: list[str] = []
    emit_prelude(lines, functions_doc["source"])
    emit_strings(lines, strings_doc["strings"])
    emit_prototypes(lines, functions)
    emit_semantic_core(lines, navigation_pairs)
    emit_remaining_functions(lines, functions, api_doc["calls"], xrefs_doc)
    emit_system_header(system_header)
    output.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"wrote {system_header}")
    print(f"wrote {output} ({len(lines)} lines, {len(functions)} recovered labels)")


if __name__ == "__main__":
    main()
