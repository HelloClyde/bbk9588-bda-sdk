def c_escape(value: str) -> str:
    return (
        value.replace("\\", "\\\\")
        .replace('"', '\\"')
        .replace("\r", "\\r")
        .replace("\n", "\\n")
        .replace("\t", "\\t")
    )


def short_text(value: str, limit: int = 100) -> str:
    escaped = c_escape(value)
    if len(escaped) > limit:
        return escaped[: limit - 3] + "..."
    return escaped


def name_for_va(va: int) -> str:
    return SEMANTIC_NAMES.get(va, f"sub_{va:08x}")


