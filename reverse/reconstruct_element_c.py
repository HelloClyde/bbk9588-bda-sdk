def load_json(path: Path) -> dict:
    return json.loads(path.read_text(encoding="utf-8"))


def load_navigation_pairs(source: Path) -> list[tuple[int, int]]:
    """Read the 119 two-word neighbor records at VA 0x81c03fb8."""
    data = source.read_bytes()
    table_offset = 0xD590
    table_size = 119 * 8
    if len(data) < table_offset + table_size:
        raise ValueError(f"BDA is too small for navigation table: {source}")
    return [
        struct.unpack_from("<II", data, table_offset + index * 8)
        for index in range(119)
    ]


