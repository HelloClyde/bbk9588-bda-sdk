def gpio_port_for_addr(addr: int) -> int | None:
    if not (GPIO_BASE <= addr < GPIO_BASE + GPIO_PORT_COUNT * GPIO_PORT_STRIDE):
        return None
    return (addr - GPIO_BASE) // GPIO_PORT_STRIDE


def gpio_addr_for_port(port: int, offset: int) -> int:
    return GPIO_BASE + port * GPIO_PORT_STRIDE + offset


def gpio_main_irq_for_port(port: int) -> int:
    return GPIO_MAIN_IRQ_BASE - port

